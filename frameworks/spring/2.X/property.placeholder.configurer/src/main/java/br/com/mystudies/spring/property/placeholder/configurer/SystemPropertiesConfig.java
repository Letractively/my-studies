package br.com.mystudies.spring.property.placeholder.configurer;

/**
 * @author robson.o.d
 */
public class SystemPropertiesConfig {

	private String osName;
	private String cpuName;
	private String cpuSpeedy;
	private String ram;
	/**
	 * Metodo get do atributo osName.
	 *
	 * @return osName
	 */
	public String getOsName() {
		return osName;
	}
	/**
	 * Metodo set do atributo osName.
	 *
	 * @param osName
	 */
	public void setOsName(String osName) {
		this.osName = osName;
	}
	/**
	 * Metodo get do atributo cpuName.
	 *
	 * @return cpuName
	 */
	public String getCpuName() {
		return cpuName;
	}
	/**
	 * Metodo set do atributo cpuName.
	 *
	 * @param cpuName
	 */
	public void setCpuName(String cpuName) {
		this.cpuName = cpuName;
	}
	/**
	 * Metodo get do atributo cpuSpeedy.
	 *
	 * @return cpuSpeedy
	 */
	public String getCpuSpeedy() {
		return cpuSpeedy;
	}
	/**
	 * Metodo set do atributo cpuSpeedy.
	 *
	 * @param cpuSpeedy
	 */
	public void setCpuSpeedy(String cpuSpeedy) {
		this.cpuSpeedy = cpuSpeedy;
	}
	/**
	 * Metodo get do atributo ram.
	 *
	 * @return ram
	 */
	public String getRam() {
		return ram;
	}
	/**
	 * Metodo set do atributo ram.
	 *
	 * @param ram
	 */
	public void setRam(String ram) {
		this.ram = ram;
	}	
}
